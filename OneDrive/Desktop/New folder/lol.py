import turtle


def rectangle():
    turtle.down()
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.done
 
    
    




def triangle():
    turtle.left(90)
    turtle.forward(100)
    turtle.left(50)
    turtle.forward(65)
    turtle.left(80)
    turtle.forward(70)
   




def house():
    rectangle()
    triangle()
    

house()

turtle.up()
turtle.home()
turtle.backward(200)
house()


def lollipop():
    turtle.up()
    turtle.home()
    turtle.forward(100)
    turtle.down()
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(200)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(200)
    turtle.right(180)
    turtle.forward(200)
    turtle.right(90)
    turtle.forward(30)
    turtle.circle(radius=60)



lollipop()


